app.controller('StudentApartelleViewController', function($scope, $routeParams, StudentApartelle) {

  $scope.id = $routeParams.id;

  $scope.data = {};

  // load 

  $scope.load = function() {

    StudentApartelle.get({ id: $scope.id }, function(e) {

      $scope.data = e.data;

      $scope.apartelleImage = e.apartelleImage;

    });

  }

  $scope.load();  

  $scope.viewImage = function(image) {

    $scope.image = image;

    $('#view-image-modal').modal('show');
    
  }

  // remove 
  $scope.remove = function(data) {

    bootbox.confirm('Are you sure you want to remove '+ data.code +' ?', function(c) {

      if (c) {

        StudentApartelle.remove({ id: data.id }, function(e) {

          if (e.ok) {

            $.gritter.add({

              title: 'Successful!',

              text:  e.msg,

            });

            window.location = "#/corporate-affairs/student-apartelle";

          }

        });

      }

    });

  } 

});